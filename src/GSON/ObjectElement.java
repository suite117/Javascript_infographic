package GSON;

public class ObjectElement {

		private String id;
		private boolean included;
		
		public String getId() {
			return id;
		}
		public void setId(String id) {
			this.id = id;
		}
		public boolean isIncluded() {
			return included;
		}
		public void setIncluded(boolean included) {
			this.included = included;
		}
}
