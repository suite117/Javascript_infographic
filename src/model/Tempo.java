package model;

import java.util.List;

public class Tempo {
	private List<Integer> start;
	private List<Integer> end;

	public List<Integer> getStart() {
		return start;
	}

	public void setStart(List<Integer> start) {
		this.start = start;
	}

	public List<Integer> getEnd() {
		return end;
	}

	public void setEnd(List<Integer> end) {
		this.end = end;
	}
}
